package entidade;

/**
 *
 * @author Eduardo
 */
public class Usuario {
    private String nome;
    private String cargo;
    private String registroDeFuncionario;

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the cargo
     */
    public String getCargo() {
        return cargo;
    }

    /**
     * @param cargo the cargo to set
     */
    public void setCargo(String cargo) {
        this.cargo = cargo;
    }

    /**
     * @return the registroDeFuncionario
     */
    public String getRegistroDeFuncionario() {
        return registroDeFuncionario;
    }

    /**
     * @param registroDeFuncionario the registroDeFuncionario to set
     */
    public void setRegistroDeFuncionario(String registroDeFuncionario) {
        this.registroDeFuncionario = registroDeFuncionario;
    }
    
    
}
